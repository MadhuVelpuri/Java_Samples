package collections;

class Student {
    int rollno, age;
    String name;

    Student(int rollno, String name, int age) {
        this.rollno = rollno;
        this.name = name;
        this.age = age;

    }

    Student(int rollno, String name) {
        this.rollno = rollno;
        this.name = name;

    }
}
